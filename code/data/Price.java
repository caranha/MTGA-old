package data;

import java.util.Date;

public class Price implements Comparable<Price>{
	
	public Date d; // Date for this price
	public Double p; // closing price
	public Double v; // volume
	
	public Price(Date dt, Double cpr, Double vo)
	{
		d = dt;
		p = cpr;
		v = vo;
	}
	
	public int compareTo(Price pr)
	{
		return d.compareTo(pr.d);
	}

}
